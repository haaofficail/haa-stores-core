import { pgTable, serial, varchar, integer, timestamp, boolean, text, decimal, jsonb, unique } from 'drizzle-orm/pg-core';
import { tenants } from './tenants.js';

export const stores = pgTable('stores', {
  id: serial('id').primaryKey(),
  tenantId: integer('tenant_id').notNull().references(() => tenants.id, { onDelete: 'cascade' }),
  name: varchar('name', { length: 255 }).notNull(),
  slug: varchar('slug', { length: 100 }).notNull(),
  description: text('description'),
  logoUrl: varchar('logo_url', { length: 500 }),
  coverUrl: varchar('cover_url', { length: 500 }),
  primaryColor: varchar('primary_color', { length: 20 }).default('#2563eb'),
  backgroundColor: varchar('background_color', { length: 20 }).default('#ffffff'),
  email: varchar('email', { length: 255 }),
  phone: varchar('phone', { length: 20 }),
  isDemo: boolean('is_demo').notNull().default(false),
  demoProfile: varchar('demo_profile', { length: 50 }),
  demoSeedVersion: varchar('demo_seed_version', { length: 50 }),
  status: varchar('status', { length: 20 }).notNull().default('active'),
  isActive: boolean('is_active').notNull().default(true),
  publishStatus: varchar('publish_status', { length: 20 }).notNull().default('draft'),
  seoTitle: varchar('seo_title', { length: 60 }),
  seoDescription: varchar('seo_description', { length: 160 }),
  city: varchar('city', { length: 100 }),
  district: varchar('district', { length: 100 }),
  street: varchar('street', { length: 255 }),
  postalCode: varchar('postal_code', { length: 20 }),
  latitude: decimal('latitude', { precision: 10, scale: 7 }),
  longitude: decimal('longitude', { precision: 10, scale: 7 }),
  policies: jsonb('policies').$type<{
    about?: string;
    privacy?: string;
    returns?: string;
    shipping?: string;
    terms?: string;
    contact?: string;
  }>(),
  createdAt: timestamp('created_at').notNull().defaultNow(),
  updatedAt: timestamp('updated_at').notNull().defaultNow(),
}, (table) => ({
  uniqueSlug: unique().on(table.slug),
}));

